  // Check if form is valid before perform login or signup
  bool validateAndSave() {
    final form = _formKey.currentState;
    if (form.validate()) {
      form.save();
      return true;
    }
    return false;
  }

  bool validateAndSave2() {
    final form = _formKey2.currentState;
    if (form.validate()) {
      form.save();
      return true;
    }
    return false;
